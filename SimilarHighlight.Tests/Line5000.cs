                }
            }
        }

        void GetRootNode()
        {
            try
            {
                // When the target file is edited and not saved, the position of selection is different from befrore.
                var currentTextDoc = Document.Object("TextDocument");
                var tmpSource = currentTextDoc.StartPoint.CreateEditPoint().GetText(currentTextDoc.EndPoint);

                TimeWatch.Init();
                // If the source code was changed, the new one must be used in the next processing.
                if (SourceCode == null || SourceCode != tmpSource || HasParseException)
                {
                    SourceCode = tmpSource;
                    TimeWatch.Start();
                    // If the converting errors, the exception will be catched.
                    RootNode = TreeGenerator.GenerateTreeFromCodeText(SourceCode, false);
                    TimeWatch.Stop("GenerateXml");
                    HasParseException = false;
                    //       TokenElements = RootNode.Descendants("TOKEN").ToList();
                }
            }
            catch (ThreadAbortException tae)
            {
                OutputMsgForExc("Background thread of highlighting is stopping.[GetRootNode method]");
            }
            catch (Code2Xml.Core.Generators.ParseException pe)
            {
                HasParseException = true;
                OutputMsgForExc("SimilarHighlight is temporary invalid because of a ParseException, " +
                                "that is likely from some serious format errors such as the lack of braces. Please fix them to use the tool.");
            }
            catch (Exception exc)
            {
                OutputMsgForExc(exc.ToString());
            }
        }

        void HighlightSimilarElements(SelectMethod selectMethod)
        {
            
            try
            {
                if (selectMethod == SelectMethod.Mouse)
                {
                    if (!GetCodeRange(out CurrentRange))
                    {
                        return;
                    }
                    TimeWatch.Start();
                    
                    // Output the selection logs.
                    OutputSelectionLogs(RequestSelection);
                    CurrentNode = CurrentRange.FindOutermostElement(RootNode);
                    TimeWatch.Stop("FindOutermostElement");
                }
                else if (selectMethod == SelectMethod.Keyboard)
                {

                    if (RequestSelection.Text.Trim() == "")
                    {
                        // When the selected text was changed by a new char from user's typing.
                        CurrentRange = PreRange;
                        CurrentNode = CurrentRange.FindOutermostElement(PreRootNode);
                        
                        // Do not loose shift key and input a uppercase character. 
                        OutputSelectionLogsFromCodeRange(CurrentRange, PrevText);
                    }
                    else
                    {
                        CurrentNode = CurrentRange.FindOutermostElement(RootNode);
                        OutputSelectionLogs(RequestSelection);
                    }
                }

                // The selected element is same with before.
                if (!BuildLocationsFromTwoElements(CurrentRange, CurrentNode))
                    return;

                PrevText = "";

                if (Locations.Count == 2)
                {
                    // Get the similar Elements.
                    var ret = InferrerSelector.GetSimilarElements(Locations,
                            RootNode, treeType, ref nodeNames);

                    PaneLineCnt = RequestSelection.TextPane.Height;
                    TimeWatch.Start();
                    // If no similar element is found then nothing will be highlighted.
                    if (ret.Count() == 0 || ret.First().Item1 == 0)
                    {
                        HaveSimilarElements = false;
                        // The element selected before current two element will be added to compare.
                        if (PreLocationInfo.CstNode != null)
                        {
                            var tmpLocationInfo = Locations[0];
                            Locations[0] = PreLocationInfo;

                            // Get the similar Elements.
                            ret = InferrerSelector.GetSimilarElements(Locations,
                                    RootNode, treeType, ref nodeNames);
                            
                            // If no similar element is found then nothing will be highlighted.
                            if (ret.Count() == 0 || ret.First().Item1 == 0)
                            {
                                Locations[0] = tmpLocationInfo;
                                return;
                            }
                        }
                        else
                        {
                            return;
                        }
                    }

                    // Highlight operation.
                    Highlight(RequestSelection, ret);
                }
            }
            catch (ThreadAbortException tae)
            {
                OutputMsgForExc(tae.ToString());
            //    OutputMsgForExc("Background thread of highlighting is stopping.[HighlightSimilarElements method]");
            }
            catch (NullReferenceException nre)
            {
                OutputMsgForExc("SimilarHighlight is temporary invalid because of a ParseException, " +
                                "that is likely from some serious format errors such as the lack of braces. Please fix them to use the tool.");
            }
            catch (Exception exc)
            {
                OutputMsgForExc(exc.ToString());
            }
        }

        bool BuildLocationsFromTwoElements(CodeRange currentRange, CstNode currentNode) 
        {
            try
            {
                if (Locations.Where(ln => ln.CstNode == currentNode).Count() > 0)
                {
                    return false;
                }

                var tmpLocationInfo = new LocationInfo
                {
                    CodeRange = currentRange,
                    CstNode = currentNode,
                    IsNeedFix = NeedFixCheck(currentNode),
                }; 

                if (Locations.Count == 2 && HaveSimilarElements)
                {
                    // Save the second element when the current two elements have similar element.
                    PreLocationInfo = Locations[1];
                    Locations.Clear();
                }
                else if (Locations.Count == 2 && !HaveSimilarElements) {
                    // Save the first element when the current two elements have not similar element.
                    PreLocationInfo = Locations[0];

                    Locations[0] = tmpLocationInfo;
                    Locations.Reverse();
                    return true;
                }

                Locations.Add(tmpLocationInfo);
            }
            catch (ThreadAbortException tae)
            {
                OutputMsgForExc("Background thread of highlighting is stopping.[BuildLocationsFromTwoElements method]");
            }
            catch (ArgumentException tae)
            {
                //  OutputMsgForExc("Background thread of highlighting is stopping.[GetTags method]");
            }
            catch (Exception exc)
            {
                OutputMsgForExc(exc.ToString());
            }
            return true;
        }

        void Highlight(TextSelection currentSelection, IEnumerable<Tuple<int, CodeRange>> ret)
        {
            try
            {
                // Have the similar elements.
                HaveSimilarElements = true;

                var currentStart = CurrentWordForCheck.Start;
                var currentEnd = CurrentWordForCheck.End;

                if (Locations[0].IsNeedFix || Locations[1].IsNeedFix || nodeNames.Contains("element_initializer"))
                {
                    FixKit = Tuple.Create(RegexNeedFix,
                                        Tuple.Create(startOffset, endOffset));
                    if (RegexNeedFix.IsMatch(CurrentWordForCheck.GetText()))
                    {
                        currentStart += 1;
                        currentEnd -= 1;
                    }
                }
                else
                {
                    FixKit = null;
                }

                if (NewSpanAll.Count > 0)
                {
                    NewSpanAll.Clear();
                }
                CurrentSelectNum = 0;
                highlightNo++;

                Parallel.ForEach(ret, tuple =>
                {
                    lock (buildLock)
                    {
                        // Build the data collecton of the similar elements.
                        BuildSimilarElementsCollection(tuple);
                    }
                });

                TimeWatch.Stop("BuildSimilarElementsCollection");
                if (NewSpanAll.Count == 0)
                {
                    return;
                }

                // Display the margin marks.
                if (OptionPage.MarginEnabled)
                {
                    this.RedrawMargin();
                }

                // Display the output datas.
                if (OutputWindow != null)
                {
                    try
                    {
                        if (this.outputThread != null && this.outputThread.IsAlive)
                        {
                            this.outputThread.Abort();                       
                        }
                        this.outputThread = new System.Threading.Thread(this.OutputSimilarDatas);
                        this.outputThread.IsBackground = true;
                        this.outputThread.Priority = ThreadPriority.Normal;
                        this.outputThread.Start();
                    }
                    catch (ThreadAbortException tae)
                    {
                        OutputMsgForExc("Output thread is stopping.");
                    }
                }

                NormalizedSnapshotSpanCollection newWordSpans = new NormalizedSnapshotSpanCollection(NewSpanAll);

                // TODO if the elements of a line is bigger than 1, the position need to fix.
                var curSelection = newWordSpans.AsParallel().Select((item, index) => new { Item = item, Index = index })
                        .FirstOrDefault(sel => (sel.Item.Start <= currentStart &&
                        sel.Item.End >= currentEnd));

                if (curSelection != null)
                {
                    CurrentSelectNum = curSelection.Index;
                    // TODO temp  added
                    TMPCurrentSelectNum = CurrentSelectNum;
                }
                
                // If another change hasn't happened, do a real update TODO:
                if (currentSelection == RequestSelection)
                    SynchronousUpdate(currentSelection, newWordSpans, CurrentWordForCheck);
            }
            catch (ThreadAbortException tae)
            {
                OutputMsgForExc("Background thread of highlighting is stopping.[Highlight method]");
            }
            catch (Exception exc)
            {
                OutputMsgForExc(exc.ToString());
            }
        }

